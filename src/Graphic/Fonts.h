#ifndef BOSCONIAN_FONTS_H
#define BOSCONIAN_FONTS_H

/**
 * @namespace Font
 * @brief Contains enumerations for different font types.
 */
namespace Font {

    /**
     * @enum Type
     * @brief Represents different font sizes and styles for the "JOYSTIX" font.
     */
    enum Type {
        JOYSTIX_128PX,   ///< Joystix font at 128 pixels.
        JOYSTIX_64PX,    ///< Joystix font at 64 pixels.
        JOYSTIX_38PX,    ///< Joystix font at 38 pixels.
        JOYSTIX_24PX,    ///< Joystix font at 24 pixels.
        JOYSTIX_16PX,    ///< Joystix font at 16 pixels.
        MAX              ///< Maximum number of font options.
    };
}

#endif //BOSCONIAN_FONTS_H
