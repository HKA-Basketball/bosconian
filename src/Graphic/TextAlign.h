#ifndef BOSCONIAN_TEXTALIGN_H
#define BOSCONIAN_TEXTALIGN_H

/**
 * @brief Specifies text alignment options.
 *
 * This enum class is used to determine how text should be aligned, either to the left, center, or right.
 */
enum class TextAlign {
    LEFT,   //!< Align text to the left.
    CENTER, //!< Align text to the center.
    RIGHT   //!< Align text to the right.
};


#endif //BOSCONIAN_TEXTALIGN_H
